public class Classroom {
    int maLop;
    String tenLop;
    int soHocSinh;

    public Classroom(int maLop, String tenLop, int soHocSinh) {
        this.maLop = maLop;
        this.tenLop = tenLop;
        this.soHocSinh = soHocSinh;
    }

    public int getMaLop() {
        return maLop;
    }

    public String getTenLop() {
        return tenLop;
    }

    public int getSoHocSinh() {
        return soHocSinh;
    }
}
